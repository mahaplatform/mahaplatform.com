import Questions from '../../components/questions'
import PropTypes from 'prop-types'
import { Form } from 'maha-admin'
import React from 'react'

class Edit extends React.Component {

  static contextTypes = {
    modal: PropTypes.object
  }

  static propTypes = {
    quiz: PropTypes.object
  }

  _handleCancel = this._handleCancel.bind(this)
  _handleSuccess = this._handleSuccess.bind(this)

  render() {
    return <Form { ...this._getForm() } />
  }

  _getForm() {
    const { quiz } = this.props
    return {
      title: 'Edit Quiz',
      method: 'patch',
      endpoint: `/api/admin/trainings/quizes/${quiz.id}/edit`,
      action: `/api/admin/trainings/quizes/${quiz.id}`,
      onCancel: this._handleCancel,
      onSuccess: this._handleSuccess,
      sections: [
        {
          fields: [
            { label: 'Title', name: 'title', type: 'textfield', required: true },
            { label: 'Questions', name: 'questions', type: Questions, required: true },
            { label: 'Passing Score', name: 'passing_score', type: 'numberfield', required: true }
          ]
        }
      ]
    }
  }

  _handleCancel() {
    this.context.modal.close()
  }

  _handleSuccess() {
    this.context.modal.close()
  }

}

export default Edit
